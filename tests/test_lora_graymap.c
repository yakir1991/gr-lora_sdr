#include <assert.h>
#include <stdio.h>
#include "lora_log.h"
#include "lora_graymap.h"

int main(void)
{
    const uint8_t sf = 7;
    const size_t len = 1u << sf;
    static const uint32_t map_lut[128] = {
        0, 1, 3, 2, 6, 7, 5, 4, 12, 13, 15, 14, 10, 11, 9, 8,
        24, 25, 27, 26, 30, 31, 29, 28, 20, 21, 23, 22, 18, 19, 17, 16,
        48, 49, 51, 50, 54, 55, 53, 52, 60, 61, 63, 62, 58, 59, 57, 56,
        40, 41, 43, 42, 46, 47, 45, 44, 36, 37, 39, 38, 34, 35, 33, 32,
        96, 97, 99, 98, 102, 103, 101, 100, 108, 109, 111, 110, 106, 107, 105, 104,
        120, 121, 123, 122, 126, 127, 125, 124, 116, 117, 119, 118, 114, 115, 113, 112,
        80, 81, 83, 82, 86, 87, 85, 84, 92, 93, 95, 94, 90, 91, 89, 88,
        72, 73, 75, 74, 78, 79, 77, 76, 68, 69, 71, 70, 66, 67, 65, 64
    };
    static const uint32_t demap_lut[128] = {
        1, 2, 4, 3, 8, 7, 5, 6, 16, 15, 13, 14, 9, 10, 12, 11,
        32, 31, 29, 30, 25, 26, 28, 27, 17, 18, 20, 19, 24, 23, 21, 22,
        64, 63, 61, 62, 57, 58, 60, 59, 49, 50, 52, 51, 56, 55, 53, 54,
        33, 34, 36, 35, 40, 39, 37, 38, 48, 47, 45, 46, 41, 42, 44, 43,
        0, 127, 125, 126, 121, 122, 124, 123, 113, 114, 116, 115, 120, 119, 117, 118,
        97, 98, 100, 99, 104, 103, 101, 102, 112, 111, 109, 110, 105, 106, 108, 107,
        65, 66, 68, 67, 72, 71, 69, 70, 80, 79, 77, 78, 73, 74, 76, 75,
        96, 95, 93, 94, 89, 90, 92, 91, 81, 82, 84, 83, 88, 87, 85, 86
    };

    uint32_t in[len];
    uint32_t out[len];
    for (size_t i = 0; i < len; ++i) {
        in[i] = i;
    }

    lora_gray_map(in, out, sf, len);
    for (size_t i = 0; i < len; ++i) {
        assert(out[i] == map_lut[i]);
    }

    lora_gray_demap(in, out, sf, len);
    for (size_t i = 0; i < len; ++i) {
        assert(out[i] == demap_lut[i]);
    }

    for (size_t i = 0; i < len; ++i) {
        uint32_t sym = demap_lut[i];
        uint32_t demod = (sym - 1) & (len - 1);
        uint32_t rec;
        lora_gray_map(&demod, &rec, sf, 1);
        assert(rec == i);
    }

    LORA_LOG_INFO("Gray map test passed");
    return 0;
}
